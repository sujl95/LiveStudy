package LiveStudy._4Week;

public class conditionalSample {
    public static void main(String[] args) {
        int score = 90;

        switch (score) {
            case 95:
                System.out.println("A+");
                break;
            case 90:
                System.out.println("A");
                break;
            case 85:
                System.out.println("B+");
                break;
        }
    }
}
